import math

# Square root
result = math.sqrt(25)
print(result)  # Output: 5.0

result = math.sqrt(9)
print(result)  # Output: 3.0


# Trigonometric functions (e.g., sine)
result = math.sin(math.pi / 2)
print(result)  # Output: 1.0

# Constants (e.g., pi)
pi_value = math.pi
print(pi_value)  # Output: 3.141592653589793
